			<p>L’accoglienza riservata ai payasitos italianos è andata ampliandosi e crescendo di edizione in edizione. La Direzione stessa di N.P.H. supporta l’attività dei volontari contribuendo alla stesura del programma della Missione, e la collaborazione con referenti in loco è andata ampliandosi sempre di più, permettendo di instaurare rapporti duraturi di stima e fiducia, fondamentali per dare continuità e sviluppo futuro a progettazione, cooperazione e sostegno.</p>

			<p>L’attività clown durante la missione permette di venire a contatto, oltre all’orfanotrofio e con bambini e le famiglie del <a href="borseStudio.php">Programma delle Borse di Studio</a> nel Dipartimento di Chimaltenango, anche con svariate altre realtà della zona: ospedali, scuole, case di riposo, centri oncologici, strutture per bambini sieropositivi e con handicap gravi, centri educativi e ricreativi per bambini che vivono in quartieri fortemente degradati.</p>

			<p>Un’esperienza decisamente intensa ed emozionante, che segna profondamente, in quanto ci si trova a fare clownterapia in realtà molto toccanti e particolari. Un’esperienza che ti convince che, grazie alle persone e alle situazioni che ti attorniano, in fondo, tutto sia un po’ più possibile.</p>
