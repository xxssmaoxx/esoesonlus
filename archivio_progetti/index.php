			<h2>PROGRAMMA RAFFORZAMENTO SCOLASTICO</h2>

			<p class="bold"><i>Progetto ufficialmente concluso nel 2015</i></p>

			<p>Durante le missioni umanitarie in Guatemala, intraprese negli anni, i volontari clown hanno avuto modo di conoscere da vicino le condizioni di evidente indigenza in cui vive la popolazione locale, in particolare durante la permanenza presso le famiglie dei bambini che aderiscono al <b>Programma di Rafforzamento Scolastico</b>, presso il villaggio di San Andrés Itzapa (nel dipartimento di Chimaltenango). Un momento forte e coinvolgente, di intensa interazione con la realtà locale.</p>

			<p><b>Nuevo Amanecer</b> (in linguaggio maya K’ak’a ‘Saqarik) significa <b>speranza di una nuova alba: nella vita dei bambini, dei genitori e degli abitanti della comunità di San Andrés</b>. Il progetto è parte integrante di un programma più ampio e complesso, coordinato in loco da assistenti sociali e volontari, finalizzato non solo a fornire una <b>formazione scolastica di base a bambini che vivono in condizioni di estrema povertà</b>, ma anche per far prendere loro coscienza della realtà in cui vivono: le tematiche su cui il programma focalizza l’attenzione sono povertà, lavoro minorile, istruzione, violenza intrafamiliare.</p>

			<p>“Nuevo Amanecer” è il nome dell’Associazione che, fondata a San Andrés nel 1989, nel 2010 si è costituita ONG, con l’obiettivo di sviluppare nelle famiglie la volontà di credere nella tutela della salute e dell’istruzione dei propri figli.</p>

			<p>Il progetto è stato sostenuto attivamente per anni all’interno dell’<a href="www.conivip.it">associazione Côni Vip Onlus</a>. Il primo passo compiuto da Côni Vip per sostenere il <b>programma di rafforzamento scolastico</b> è stata la costruzione della tettoia dove i bambini del doposcuola vengono radunati per fare i compiti e le attività inerenti il Progetto, il tutto consentito soprattutto grazie alle donazioni derivate dallo spettacolo teatrale <b><i>La ballata di Grillo Talpa</i></b> (tenutosi al Teatro Toselli di Cuneo nel dicembre 2008).</p>

			<p>Successivamente, in seguito alla <b>Mostra</b> e alla pubblicazione del <b>volume fotografico <i>Niños</i></b>, grazie nuovamente alla sensibilità e alla generosità di numerose donazioni, l’associazione è riuscita a dar vita ad un <b>progetto di <i>apadrinamento</i></b>, contestualmente al progetto di rafforzamento scolastico.</p>
			<p>&nbsp;</p>
			<p>Il progetto si è concluso definitivamente nel corso del 2015.</p>
