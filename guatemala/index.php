				<p>La popolazione del Guatemala è composta da 22 diversi gruppi etnici che discendono dall’antica popolazione maya e dai cosiddetti “ladinos”, cioè dagli europei giunti in questo paese nei secoli scorsi. Nonostante le difficoltà e le ostilità, gli indios sono stati in grado di preservare nei secoli la loro cultura e le loro tradizioni.</p>

