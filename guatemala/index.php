		<p>In Guatemala solo il 56% della popolazione sa leggere e scrivere (in Italia è del 97%). Le scuole sono poche e non sono sufficienti, in particolare nelle zone rurali.</p>

		<p>Frequentare la scuola è difficile, spesso gli edifici scolastici sono lontani dai villaggi, e i bambini devono percorrere molti chilometri a piedi per raggiungerli.</p>

		<p>Le classi sono numerose, e il numero degli insegnanti è esiguo, poiché ve ne è uno ogni 35-40 bambini circa. Anche se lo Stato ha sancito l’obbligo scolastico, vi è un’alta percentuale di bambini (circa l’8%), nelle zone rurali che, pur essendo in età scolare, non viene iscritta a scuola.</p>

		<p>La maggior parte è costituita da donne, in quanto le famiglie ritengono spesso che sia inutile far studiare le bambine, e preferiscono impiegarle nei lavori domestici.</p>

		<p>Le scuole sono di fatto, quindi, poco frequentate sia a causa dei costi elevati dei trasporti, dei libri e dei materiali, sia perché non si garantisce l’insegnamento nelle lingue nazionali diverse dallo spagnolo (nonostante 22 lingue maya e non maya siano state riconosciute con una legge del 2003 dal governo guatemalteco).</p>

		<p>Le politiche pubbliche non prevedono dei programmi permanenti di formazione extra scolastica e di formazione professionale.</p>

		<p>La mancanza di un adeguato livello di istruzione, oltre alle basse opportunità di occupazione, costringono i giovani ad abbandonare la carriera scolastica, per cercare lavoro nell’economia informale.</p>

		<p>I problemi che affliggono i bambini, gli adolescenti e i giovani sono la malnutrizione, la mancanza di cure mediche, di accesso all’istruzione e alla ricreazione, la violenza domestica, la violenza e lo sfruttamento sessuale, lo sfruttamento economico, i rischi associati agli alti tassi di violenza criminale che coinvolgono questo settore della popolazione, sia come vittima di azioni violente o come partecipante alle maras (gruppi di criminalità organizzata).</p>
